
mi_lista = [5, 7, 2, 11, 3, 9]
print(mi_lista[1])  # imprime el valor según el índice de la lista

mi_lista[1] = 10 # cambia el valor según el índice
print(mi_lista)

mi_lista.append(20) # agrega el valor 20 al final de la lista
print(mi_lista)

mi_lista.remove(11) # elimina el valor 11 de la lista.
print(mi_lista)









